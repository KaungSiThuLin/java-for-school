public class Practice2_2 {
    public static void main(String[] args) {
        int i = 2;
        int j = 5;
        // j = j * i; => j => 10
        j *= i;
        // k = 10;
        int k = j;
        // k = k / 2
        // k = 10 / 2
        k /= 2;
        // k = 5
        System.out.println(k);
    }
}
