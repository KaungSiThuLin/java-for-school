public class TypeExample2 {
    public static void main(String[] args) {
        int i = 10;
        double d = 3.14;
        String c = "Hello, World!";
        boolean e = true;

        System.out.println("Integer: " + i);
        System.out.println("Double: " + d);
        System.out.println("String: " + c);
        System.out.println("Boolean: " + e);
    }
}
